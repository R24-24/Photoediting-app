const translations = {
  en,
  hi,
  mr
};

type TranslationKeys = keyof typeof en;

export const useTranslation = () => {
  const { language } = useLanguage();

  const t = useCallback(
    (key: TranslationKeys) => {
      return translations[language][key] || translations['en'][key] || key;
    },
    [language]
  );

  return { t };
};